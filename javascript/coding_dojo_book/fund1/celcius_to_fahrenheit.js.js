//Q: create a celciusToFahrenheit(cDeg) function that accepst a number of degrees Celcius and returns the equivilent teperature expressed in Fahrenheit degrees: Fahrenheit = (9/5 * Celcius) + 32

//A:
function celciusToFahrenheit(cDeg) {
    return (9/5 * cDeg) + 32
}

